#include<iostream>
#include<queue>
using namespace std;

int main() {
// create queue of strings names;
	queue<string> names;

	// push names in the queue;
	names.push("Craciun");
	names.push("Radu");
	names.push("Mihai");

	cout << "Queue with names: ";

	// print the names on display;
	while (!names = NULL) {
		cout << names.front() << ", ";

	}
	cout << endl;
	return 0;

}

